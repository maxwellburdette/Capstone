class Room {
    constructor(roomNumber, bedType, bedCount, maxOccupancy, cost, viewRoom)
    {
        this.roomNumber = roomNumber;
        this.bedType = bedType;
        this.bedCount = bedCount;
        this.maxOccupancy = maxOccupancy;
        this.cost = cost;
        this.viewRoom = viewRoom;
    }
}